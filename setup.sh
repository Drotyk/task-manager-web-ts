#!/bin/bash

# Скрипт для швидкого налаштування та запуску проекту

echo "🚀 Починаємо налаштування Task Manager (TS)..."

# Перевірка наявності npm
if ! command -v npm &> /dev/null
then
    echo "❌ Помилка: npm не знайдено. Будь ласка, встановіть Node.js."
    exit
fi

# Встановлення залежностей
echo "📦 Встановлення залежностей..."
npm install

# Створення папки для БД, якщо її немає
mkdir -p data

# Запуск у режимі розробки
echo "🏃 Запуск сервера..."
npm run dev
